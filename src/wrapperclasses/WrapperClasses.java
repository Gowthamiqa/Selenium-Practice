package wrapperclasses;

public class WrapperClasses {

	public static void main(String[] args) {
  
		String s="10";
		System.out.println(s+10);
		  Integer.parseInt(s);
		System.out.println(Integer.parseInt(s)+10);
		
		String y="100";
		System.out.println(y+20);
		Double.parseDouble(y);
		System.out.println(Double.parseDouble(y)+20);
		
		String z="false";
		System.out.println(z+20);
		Boolean.parseBoolean(z);
		System.out.println(Boolean.parseBoolean(z));
		
		int i=100;
		System.out.println(i+20);
		String.valueOf(i);
		System.out.println(String.valueOf(i)+10);
		
		
		double p=99.987;
		System.out.println(p+20);
		String.valueOf(p);
		System.out.println(String.valueOf(p)+11);
		
		char q='o';
		System.out.println(q+20);
		String.valueOf(q);
		System.out.println(String.valueOf(q)+12);
		
		boolean b=true;
		System.out.println(b);
		String.valueOf(b);
		System.out.println(String.valueOf(b)+10);
		
		
		String s1="10a";
		System.out.println(s1+10);
		Integer.parseInt(s1);
		System.out.println(Integer.parseInt(s1)+10);
		
		
		
		
	}

}
